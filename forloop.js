let i, j, k, l;
console.log(" printing from 3 to 8");
for (i = 3; i <= 8; i++) {
    console.log(i);
}

console.log(" divided by 2 between 14 and 20");
for (j = 14; j <= 20; j++) {
    if (j % 2 == 0) {
        console.log(j);
    }
}

console.log("rev printing 12 to 7");
for (k = 12; k >= 7; k--) {
    console.log(k);
}

console.log("Divided by 3 between 33 and 45 in reverse order");
for (l = 45; l >= 33; l--) {
    if (l % 3 == 0) {
        console.log(l);
    }
}